def get_location_by_name(name: str):
    """Get location by name from Metro Manila places"""
    name_lower = name.lower().strip()
    

    for location in METRO_MANILA_PLACES:
        if location['name'].lower() == name_lower:
            return location
    

    for location in METRO_MANILA_PLACES:
        if name_lower in location['name'].lower():
            return location
    
    return None

def get_all_locations():

    return METRO_MANILA_PLACES

def get_high_risk_locations():

    return [loc for loc in METRO_MANILA_PLACES if loc['suspensionRisk'] > 70]

def get_locations_by_district(district: str):

    return [loc for loc in METRO_MANILA_PLACES if loc['district'] == district]

def get_location_coordinates(name: str):

    location = get_location_by_name(name)
    if location:
        return {
            'lat': location['lat'],
            'lon': location['lon']
        }
    return None
